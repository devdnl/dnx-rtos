//==============================================================================
/**
 * Main program function.
 *
 * @param argc      argument count
 * @param argv      arguments
 */
//==============================================================================
int main(int argc, char *argv[])
{
        if (argc == 1) {
                printf("Usage: %s <term-no>\n", argv[0]);
        } else {
                int term = atoi(argv[1]);
                ioctl(fileno(stdout), IOCTL_TTY__SWITCH_TTY_TO, &term);
        }

        return EXIT_SUCCESS;
}

